    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name=customer.getText();
        String email=mail.getText();
        String mobile=number.getText();
        String adr=address.getText();
        String regex = "^[\\w!#$%&'*+/=?`{|}~^-]+(?:\\.[\\w!#$%&'*+/=?`{|}~^-]+)*@(?:[gmail.com]+\\.)+{2,6}";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(mail.getText());
        int a = number.getText().length();
        Pattern p = Pattern.compile("[7-9][0-9]{9}");
        Matcher m = p.matcher(number.getText());
        if (customer.getText().length() <=2) {   // First Name Validation
            JOptionPane.showMessageDialog(null, "Name Required", "Error", JOptionPane.ERROR_MESSAGE);
            customer.setText("");
        } 
        else if (a < 10 || a > 10) {
            if (a == 0) {
                JOptionPane.showMessageDialog(null, "Mobile No Required !", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (a < 10) {
                JOptionPane.showMessageDialog(null, "Invalid Mobile No!", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (a > 10) {
                JOptionPane.showMessageDialog(null, "Invalid Mobile No!", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (m.find() && !m.group().equals(number.getText())) {
                JOptionPane.showMessageDialog(null, "Valid");
            } 
            else if (mail.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Email Must Be Required !", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!(matcher.matches())) {
            JOptionPane.showMessageDialog(null, "Enter Mail ID in Valid Format", "Error", JOptionPane.ERROR_MESSAGE);
            } 
        }
        else if (address.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Required Residential Address", "Error", JOptionPane.ERROR_MESSAGE);
            address.setText("");
        } else if (address.getText().length() < 20) {
            JOptionPane.showMessageDialog(null, "Invalid Residential Address", "Error", JOptionPane.ERROR_MESSAGE);
            address.setText("");
        } else
        {
           new Invoice(name,email,mobile,adr,type,Price,glass,total,jn).setVisible(true);
           this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

