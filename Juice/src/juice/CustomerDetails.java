    private void customerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_customerKeyPressed
       char c = evt.getKeyChar();
        if (Character.isLetter(c) || Character.isISOControl(c) || Character.isWhitespace(c)) {
            customer.setEditable(true);
        } else {
            customer.setEditable(false);
        }
    }//GEN-LAST:event_customerKeyPressed

    private void numberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numberKeyPressed
         char c = evt.getKeyChar();
        if (Character.isDigit(c) || Character.isISOControl(c)) {
            number.setEditable(true);
        } else {
            number.setEditable(false);
        }
    }//GEN-LAST:event_numberKeyPressed

    private void mailKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mailKeyPressed
        char c = evt.getKeyChar();
        if (Character.isWhitespace(c)) {
            mail.setEditable(false);
        } else {
            mail.setEditable(true);
        }
    }//GEN-LAST:event_mailKeyPressed

    private void addressKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_addressKeyTyped
        char c = evt.getKeyChar();
        if (Character.isLetter(c) || Character.isWhitespace(c) || Character.isDigit(c)) {
            customer.setEditable(true);
        } else {
            customer.setEditable(false);
        }
    }//GEN-LAST:event_addressKeyTyped

